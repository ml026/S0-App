    //  Enviar datos BBDD

    public void enviar(TramaIBeacon tib) {

        JSONObject jsonObject = new JSONObject();
        try {
            String timeStamp = new SimpleDateFormat("yyyy/MM/dd HH:mm:ss").format(Calendar.getInstance().getTime());
            jsonObject.put("medida",Utilidades.bytesToHexString(tib.getMajor( )));
            jsonObject.put("fecha", timeStamp);

        } catch (JSONException e) {
            e.printStackTrace();
        }

        AndroidNetworking.post("http://192.168.1.30:8080/insertarMedicion")
                .addJSONObjectBody(jsonObject) // posting json
                .setTag("test")
                .setPriority(Priority.MEDIUM)
                .build()
                .getAsJSONArray(new JSONArrayRequestListener() {
                    @Override
                    public void onResponse(JSONArray response) {
                        // do anything with response
                    }
                    @Override
                    public void onError(ANError error) {

                     //   Log.d("clienterestandroid", " post: Error with Message: " + error.getErrorDetail()+error.getMessage());
                    }
                });
    }

